<h1>Laurel Schwulst</h1>

<p>
    Laurel Schwulst is an American artist, writer, and professor. 
    Selected works include “How to Build a Bird Kite” (interactive for the New York Times, 2021), 
    Flight Simulator (app for iOS and Android published by Soft, 2019), and Perfume Area (book co-written with Sydney Shen, 
    published by Ambient Works, 2015).
</p>

