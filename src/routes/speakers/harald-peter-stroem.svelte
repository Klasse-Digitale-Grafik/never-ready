<p>
    Peter Ström (1978) is a graphic / web designer and educator based in Stockholm, Sweden.
    Peter started out exploring and participating in the early web in the mid 1990s, as part of the first generation growing up online. He launched web publications such as Harald.net, Reload.org and (the infamous) Alfabag.com. He later founded web design agency Speedway (later Abel & Baker), 	spent a few years in Berlin and eventually studied graphic design at the Rietveld Academy in Amsterdam. After graduating in 2006, he relocated to Stockholm and launched graphic design studio Konst & Teknik with Mattias Jakobsson, with the aim to intersect traditional graphic design and digital design into one practice.
    Since 2016 he is senior lecturer in Visual Communication and part of the faculty at Beckmans College of Design where he teaches and researches design for screens.
    In 1998 he was named Swedens Coolest Webdesigner by industry publication Cap & Design. Exactly 20 years later, in 2018, he was awarded the Berling Price for his work bridging the digital and analogue typography in Sweden.
    Currently (2022) Peter works on a few design commussions with Konst & Teknik, researches the preservation of digital design as well as being an active board member at contemporary art gallery Issues. 	He is also a slow learner and intrigued sceptic on all thing Web3.
